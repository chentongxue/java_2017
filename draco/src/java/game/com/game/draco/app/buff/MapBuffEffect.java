package com.game.draco.app.buff;

public class MapBuffEffect extends BuffEffect{
	private int x ;
	private int y ;
	
	public MapBuffEffect(short buffId,int lv,int x,int y) {
		super(buffId);
		this.buffLv = lv ;
		this.x = x ;
		this.y = y ;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	
	
}
