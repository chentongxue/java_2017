package sacred.alliance.magic.app.goods.exception;

import sacred.alliance.magic.core.exception.ServiceException;

public class GoodsIsOnlyException extends ServiceException {
	/**
	 * 
	 */
	private static final long serialVersionUID = -8612460965979775748L;
	/**
	 * 
	 */
	public GoodsIsOnlyException(){
		super("Out Of Goods Bag Error !");
		
	}
	public GoodsIsOnlyException(Throwable e){
		super("Out Of Goods Bag Error !",e);
	}
	
	public GoodsIsOnlyException(String message){
		super(message);
		
	}
	public GoodsIsOnlyException(String message,Throwable e){
		super(message,e);
		
	}
}
