package sacred.alliance.magic.app.map.data;

import java.util.ArrayList;
import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "MapRoad")
@XmlAccessorType(XmlAccessType.FIELD)
public class MapRoadVOConfig {
	@XmlElement(name = "points")
	List<MapRoadPointVO> points = new ArrayList<MapRoadPointVO>();

	public List<MapRoadPointVO> getPoints() {
		return points;
	}

	public void setPoints(List<MapRoadPointVO> points) {
		this.points = points;
	}
}
