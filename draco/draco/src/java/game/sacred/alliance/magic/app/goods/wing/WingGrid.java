package sacred.alliance.magic.app.goods.wing;

import lombok.Data;

/**
 * 翅膀的命格信息
 *
 */
public @Data class WingGrid {
	private int wingGridId;
	private int curExp;
	private int level;
	
	public WingGrid(){
		
	}
	
	public WingGrid(int wingGridId, int curExp, int level){
		this.wingGridId = wingGridId;
		this.curExp = curExp;
		this.level = level;
	}
}
