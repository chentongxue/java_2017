public class Hatred {

	private String srcRoleId ;
	private String targetRoleId ;
	private int value = 0 ;
	
	public Hatred(){
		
	}
	
	public Hatred(String srcRoleId,String targetRoleId){
		this.srcRoleId = srcRoleId ;
		this.targetRoleId = targetRoleId ;
	}
	
	public Hatred(String srcRoleId,String targetRoleId,int value){
		this.srcRoleId = srcRoleId ;
		this.targetRoleId = targetRoleId ;
		this.value = value ;
	}
	
	public String getSrcRoleId() {
		return srcRoleId;
	}
	public void setSrcRoleId(String srcRoleId) {
		this.srcRoleId = srcRoleId;
	}
	public String getTargetRoleId() {
		return targetRoleId;
	}
	public void setTargetRoleId(String targetRoleId) {
		this.targetRoleId = targetRoleId;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
	
}
