package sacred.alliance.magic.app.active;

import java.util.HashSet;
import java.util.Set;

public class ActiveInitContext {

	/**
	 * 验证活动类型的Set
	 */
	Set<Byte> activeTypeSet = new HashSet<Byte>();
	/**
	 * 验证活动地图类型，所配置的地图唯一
	 */
	Set<String> activeMapSet = new HashSet<String>();
	
	public Set<Byte> getActiveTypeSet() {
		return activeTypeSet;
	}
	public void setActiveTypeSet(Set<Byte> activeTypeSet) {
		this.activeTypeSet = activeTypeSet;
	}
	public Set<String> getActiveMapSet() {
		return activeMapSet;
	}
	public void setActiveMapSet(Set<String> activeMapSet) {
		this.activeMapSet = activeMapSet;
	}
	
	
}
