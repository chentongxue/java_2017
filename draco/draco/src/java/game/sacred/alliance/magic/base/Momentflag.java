package sacred.alliance.magic.base;

public enum Momentflag {

	NORMAL(0,"普通"),
	START(1,"开始"),
	;
	public int type;
	
	public String name;
	
	Momentflag(int type, String name){
		this.type = type;
		this.name = name;
	}
	
	public int getType() {
		return type;
	}

	public String getName() {
		return name;
	}
	
}
